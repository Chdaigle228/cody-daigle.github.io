function drawChessboard(size) {
  var string = '';
  for(var i = 0; i < size; i++){
    for(var r = 0; r < size; r++){
      if((r + (i % 2)) % 2){
        string += '#';
      } else {
        string += ' ';
      }
    }  
  
      string += '\n';
    
  } console.log(string);

  
}

